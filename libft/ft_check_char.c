#include "libft.h"

int		ft_check_char(const char *s, char c)
{
	int		i;

	if (s)
	{
		i = 0;
		while (s[i] != '\0')
		{
			if (s[i] == c)
				return (1);
			i++;
		}
	}
	return (0);
}
